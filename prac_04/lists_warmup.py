numbers = [3, 1, 4, 1, 5, 9, 2]

# Change the first element of numbers to "ten"
numbers[0] = 10
print(numbers)

# Change the last element of numbers to 1
numbers[-1] = 1
print(numbers)

# Get all the elements from numbers except the first two
print(numbers[2:])

# Check if 9 is an element of numbers
print(9 in numbers)
